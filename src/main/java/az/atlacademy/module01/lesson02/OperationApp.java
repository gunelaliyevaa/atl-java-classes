package az.atlacademy.module01.lesson02;

public class OperationApp {
    public static void main(String[] args) {

        int a = 14;
        int b = 6;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(++a);
        System.out.println(--b);


    }
}
